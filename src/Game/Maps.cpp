Maps* Maps::_instance;
std::map<std::string, MapData> Maps::_mapList;

Maps::Maps(){
    try{
        if(_instance != nullptr){
            throw "Only one Maps is allowed.";
        }
    } catch (std::string msg) {}
    _mapList = {
        {"titleMap", titleMap},
        {"demoMap", demoMap},
        {"map01", map01},
        {"visualTestMap", visualTestMap}
    };

    _instance = this;
}

MapData Maps::GetMap(std::string mapName){
    return _mapList[mapName];
}